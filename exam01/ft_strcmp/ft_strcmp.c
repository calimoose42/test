#include <stdio.h>

int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int j;
	int result;

	i = 0;
	j = 0;
	while (*(s1 + i) != '\0')
	{
		i++;
	}
	while (*(s2 + j) != '\0')
	{
		j++;
	}
	result = i - j;
	return (result);
}
