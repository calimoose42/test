int		max(int *tab, unsigned int len)
{
	int i;
	int max;

	i = 0;
	max = 0;
	while (i < len)
	{
		if (tab[i] > tab[i + 1] && tab[i] > max)
			max = tab[i];
		else if (tab[i] < tab[i + 1] && tab[i + 1] > max)
			max = tab[i + 1];
		i++;
	}
	return (max);
}
